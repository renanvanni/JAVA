package exercicio1;

public class Cavalo extends Animal {
	
	private String tamanhaAnca;
	private String posicaoOrelhas;
	
	public String getTamanhaAnca() {
		return tamanhaAnca;
	}
	public void setTamanhaAnca(String tamanhaAnca) {
		this.tamanhaAnca = tamanhaAnca;
	}
	public String getPosicaoOrelhas() {
		return posicaoOrelhas;
	}
	public void setPosicaoOrelhas(String posicaoOrelhas) {
		this.posicaoOrelhas = posicaoOrelhas;
	}

}
