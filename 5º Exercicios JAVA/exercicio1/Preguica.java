package exercicio1;

public class Preguica extends Animal {

	private String corSorriso;
	private String manchasPelo;
	
	public String getCorSorriso() {
		return corSorriso;
	}
	public void setCorSorriso(String corSorriso) {
		this.corSorriso = corSorriso;
	}
	public String getManchasPelo() {
		return manchasPelo;
	}
	public void setManchasPelo(String manchasPelo) {
		this.manchasPelo = manchasPelo;
	}
}
